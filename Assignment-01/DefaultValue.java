public class DefaultValue{
    static int intValue;
    static String stringValue;
    static double doubleValue;
    static int[] intArray;
    
    public static void main(String[] args) {
        System.out.println("Default value of int: " + intValue);
        System.out.println("Default value of String: " + stringValue);
        System.out.println("Default value of double: " + doubleValue);
        System.out.println("Default value of int array: " + intArray);
    }
}
